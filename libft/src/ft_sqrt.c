#include "libft.h"

int		ft_sqrt(int nb)
{
	int sq;

	if (nb < 0)
		return (0);
	sq = 1;
	while (sq <= nb / 2)
	{
		if (sq * sq == nb)
			return (sq);
		else
			sq++;
	}
	return (0);
}
